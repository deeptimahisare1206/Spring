package com.mypackage;

public class Student {

	private int s_id;
	private String s_name;
	private float s_percent;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public float getS_percent() {
		return s_percent;
	}
	public void setS_percent(float s_percent) {
		this.s_percent = s_percent;
	}
	
	
}
